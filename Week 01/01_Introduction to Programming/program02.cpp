#include <iostream>
using namespace std;

int main()
{
    int gum;
    cout << gum << endl; // Garbage Value
    int sum;
    sum = 90;
    cout << sum << endl;
    int num = 50;
    cout << num << endl;
    cout << sizeof(num) << endl;
    char ch = 'k';
    cout << ch << endl;
    cout << sizeof(ch) << endl;
    float point = 1.69;
    cout << point << endl;
    cout << sizeof(point) << endl;
    long number = 23;
    cout << number << endl;
    cout << sizeof(number) << endl;
    return 0;
}